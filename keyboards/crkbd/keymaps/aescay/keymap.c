void suspend_power_down_user() {
    oled_off();
}

void oled_render_layer_state(void) {
    
    /* Print current layer */   
    oled_set_cursor(0, 1);
    oled_write("LAYER", false);

    oled_set_cursor(0, 2);

    switch (get_highest_layer(layer_state)) {
        case _COLEMAK:
            oled_write("Base ", false);
            break;
        case _MOVE:
            oled_write("Move ", false);
            break;
        case _SYMBOLS:
            oled_write("Symbl", false);
            break;
        case _NUMBERS:
            oled_write("Nums ", false);
            break;
        case _WINDOW:
            oled_write("Windw", false);
            break;
        default:
            oled_write("Undef", false);
    }
    
    /* wpm counter */
    static char wpm_str[4];

    sprintf(wpm_str, "%03d", get_current_wpm());
    oled_set_cursor(0, 9);
    oled_write(wpm_str, false);

    oled_set_cursor(0, 10);
    oled_write(" wpm", false);

   
    /* KEYBOARD PET RENDER START */

    render_luna(0, 13);

    /* KEYBOARD PET RENDER END */
}

void oled_render_logo(void) {
    /* Print dbt Logo */
    oled_set_cursor(0, 0);
    oled_write_raw_P(chrome_trex, sizeof(chrome_trex));

    /* Keeb Name */
    oled_set_cursor(0, 4);
    oled_write("andrw", false);
    oled_set_cursor(0, 5);
    oled_write("crkbd", false);

    /* Print dbt Logo */
    oled_set_cursor(0, 10);
    oled_write_raw_P(dbt_logo, sizeof(dbt_logo));

}

bool oled_task_user(void) {

    /* KEYBOARD PET VARIABLES START */

    current_wpm   = get_current_wpm();
    led_usb_state = host_keyboard_led_state();

    /* KEYBOARD PET VARIABLES END */
    if (is_keyboard_master()) {
        oled_render_layer_state();
    } else {
        oled_render_logo();
    }
    return true;
}
